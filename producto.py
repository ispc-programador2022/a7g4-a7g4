#3- función producto, retorna el producto de 2 parámetros.
valor1=int(input("Ingresar el primer número que desea multiplicar: "))
valor2=int(input("Ingresar el segundo número por el cual desea multiplicar: "))

def producto (valor1, valor2):
    return (valor1 * valor2)


print("El resultado de su operación es:: ",producto(valor1, valor2))
